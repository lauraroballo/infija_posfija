    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
    String expresionInfija = jTextField1.getText();
    String mensaje = Infija_posfija.setExpresionMatematica(expresionInfija);
    Infija_posfija.setMensaje(mensaje);
    
    // Si la expresión está balanceada, muestra la ventana resultado_de_balanceado
    if (mensaje.equals("La expresión está balanceada.")) {
        resultado_de_balanceado resultado = new resultado_de_balanceado();
        resultado.setMensaje(mensaje);
        resultado.mostrarExpresionPosfija(Infija_posfija.getExpresionPosfija());
        resultado.setVisible(true);
        this.dispose(); 
    } else {
        // Si la expresión no está balanceada, muestra un JOptionPane
        JOptionPane.showMessageDialog(this, mensaje, "Expresión No Balanceada", JOptionPane.ERROR_MESSAGE);
    }

    }//GEN-LAST:event_jButton1ActionPerformed


